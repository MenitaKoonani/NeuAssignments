    private void cancelBookingBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelBookingBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = manageBookingTable.getSelectedRow();
        if (selectedRow >= 0) {
            Customer selectedCustomer = (Customer) manageBookingTable.getValueAt(selectedRow, 0);
            Flight flight = travelAgency.getAirlinerDirectory().getFlightByAirlinerNFlightName(selectedCustomer.getAirlinerName(), selectedCustomer.getAirplaneName());
            flight.setSeatAvailable((flight.getSeatAvailable() + selectedCustomer.getTotalSeatsBooked()));
            travelAgency.getCustomerDirectory().removeCustomer(selectedCustomer);
            JOptionPane.showMessageDialog(null, "Booking cancelled! ");
            populateCustomerTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row! ", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_cancelBookingBtnActionPerformed

