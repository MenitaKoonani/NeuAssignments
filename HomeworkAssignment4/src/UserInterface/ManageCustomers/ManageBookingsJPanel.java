    public void populateCustomerTable() {
        DefaultTableModel jTable = (DefaultTableModel) manageBookingTable.getModel();
        jTable.setRowCount(0);

        for (Customer eachCustomer : travelAgency.getCustomerDirectory().getCustomerList()) {
            Object row[] = new Object[6];
            row[0] = eachCustomer;
            row[1] = eachCustomer.getLastName();
            row[2] = eachCustomer.getTotalSeatsBooked();

            jTable.addRow(row);
        }
    }

