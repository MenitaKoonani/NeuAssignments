    private void viewAirlinerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewAirlinerBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = manageAirlinerTable.getSelectedRow();
        if (selectedRow >= 0) {
            Airliner selectedAirliner = (Airliner) manageAirlinerTable.getValueAt(selectedRow, 0);
            ViewAirlinerJPanel viewAirlinerPanel = new ViewAirlinerJPanel(CardSequenceJPanel, selectedAirliner, travelAgency.getAirlinerDirectory());
            CardLayout cardLayout = (CardLayout) CardSequenceJPanel.getLayout();
            CardSequenceJPanel.add("ViewAirlinerPanel", viewAirlinerPanel);
            cardLayout.next(CardSequenceJPanel);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row! ", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_viewAirlinerBtnActionPerformed

