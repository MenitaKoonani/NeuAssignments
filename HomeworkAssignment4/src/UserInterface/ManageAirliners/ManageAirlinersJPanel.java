    public void populateManageAirlinerTable() {
        DefaultTableModel jTable = (DefaultTableModel) manageAirlinerTable.getModel();
        //starting the population form row 0
        jTable.setRowCount(0);

//        if (!airlinerDirectory.getAirlinerList().isEmpty()) {
        for (Airliner eachAirliner : travelAgency.getAirlinerDirectory().getAirlinerList()) {
            Object row[] = new Object[4];
            //setting the object to the first row as we need to access all the other attributes of that particular object
            row[0] = eachAirliner;
            row[1] = eachAirliner.getAirlinerName();
            row[2] = eachAirliner.getAirlinerAddress();
            row[3] = eachAirliner.getFlightCount();
            jTable.addRow(row);
        }
//        } else {
//            JOptionPane.showMessageDialog(null, "Airliner Directory is Empty!! ", "Warning", JOptionPane.WARNING_MESSAGE);
//        }
    }
