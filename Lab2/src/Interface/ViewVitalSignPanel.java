    private void viewVitalSignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewVitalSignBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableModel.getSelectedRow();
        if (selectedRow >= 0) {
            VitalSigns vs = (VitalSigns) tableModel.getValueAt(selectedRow, 0);
            BPField.setText(String.valueOf(vs.getBloodPressure()));
            tempField.setText(String.valueOf(vs.getTemperatue()));
            pulseField.setText(String.valueOf(vs.getPulse()));
            dateField.setText(String.valueOf(vs.getDate()));
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row!!");
        }
    }//GEN-LAST:event_viewVitalSignBtnActionPerformed

    private void deleteVitalSignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteVitalSignBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableModel.getSelectedRow();
        if (selectedRow >= 0) {
            VitalSigns vs = (VitalSigns) tableModel.getValueAt(selectedRow, 0);
            vitalSignHistory.deleteVitals(vs);
            JOptionPane.showMessageDialog(null, "Vital sign is deleted!");
            populateTable();
            
            tempField.setText("");
            BPField.setText("");
            pulseField.setText("");
            dateField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row!!");
        }
    }//GEN-LAST:event_deleteVitalSignBtnActionPerformed


