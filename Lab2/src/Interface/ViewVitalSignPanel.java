    public void populateTable() {
        DefaultTableModel jTable = (DefaultTableModel) tableModel.getModel();
        //starting the population form row 0
        jTable.setRowCount(0);

        for (VitalSigns eachVitalSign : vitalSignHistory.getVitalSignArrayList()) {
            Object row[] = new Object[2];
            //setting the object to the first row as we need to access all the other attributes of that particular object
            row[0] = eachVitalSign;
            row[1] = eachVitalSign.getBloodPressure();
            jTable.addRow(row);
        }
    }

