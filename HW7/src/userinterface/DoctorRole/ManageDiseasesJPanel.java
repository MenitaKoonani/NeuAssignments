    private void addDiseaseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDiseaseBtnActionPerformed
        String diseaseName = diseaseNameField.getText();
        if (diseaseName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Disease name cannot be empty!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        enterprise.getDiseaseCatalog().newDisease(diseaseName);
        diseaseNameField.setText("");
        populateDiseaseTable();
    }//GEN-LAST:event_addDiseaseBtnActionPerformed

