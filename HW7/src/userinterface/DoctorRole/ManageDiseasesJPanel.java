    public void populateDiseaseTable() {
        DefaultTableModel jTable = (DefaultTableModel) diseaseTable.getModel();
        jTable.setRowCount(0);
        for (Disease eachDisease : enterprise.getDiseaseCatalog().getDiseaseList()) {
            Object[] row = new Object[2];
            row[0] = eachDisease;
            jTable.addRow(row);
        }
    }

