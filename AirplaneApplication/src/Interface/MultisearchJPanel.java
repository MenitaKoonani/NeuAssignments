    private void multiSearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_multiSearchBtnActionPerformed
        // TODO add your handling code here
        errorMsgLabel.setText("");
        String queryName = searchQueryField.getText();
        if (!queryName.isEmpty() || getSearchBtnSelected() == 5) {
            searchList = MainJFrame.airplaneCatalog.getMultiSearchList(queryName, getSearchBtnSelected());
            numMatchesField.setText(String.valueOf(searchList.size()));
            populateMultiSearchTable(searchList);
        } else {
            errorMsgLabel.setText("Invalid search item. Please try again!");
            searchQueryField.requestFocus();
        }
    }//GEN-LAST:event_multiSearchBtnActionPerformed
    private int getSearchBtnSelected()
    {
        int searchBtn = 1;
        if (airlineBtn.isSelected()) {
            searchBtn = 1;
        } else if (airplaneBtn.isSelected()) {
            searchBtn = 2;
        } else if (airportBtn.isSelected()) {
            searchBtn = 3;
        } else if (manuBtn.isSelected()) {
            searchBtn = 4;
        } else if (availBtn.isSelected()) {
            searchBtn = 5;
        }
        return searchBtn;
    }
